// Atividade 1: Criando um Módulo de Calculadora

// Soma

export const soma = (a, b) => {
    return a + b;
}

// Subtração

export const subtracao = (a, b) => {
    return a - b;
}

// Multiplicação

export const multiplicacao = (a, b) => {
    return a * b;
}

// Divisão

export const divisao = (a, b) => {
    return a / b;
}